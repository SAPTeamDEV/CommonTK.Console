namespace SAPTeam.CommonTK.Console
{
    /// <summary>
    /// Represents the relative vertical coordinate.
    /// </summary>
    public enum ConsolePosition
    {
        /// <summary>
        /// Represents a static position.
        /// </summary>
        None,

        /// <summary>
        /// Represents a top-relative coordinate.
        /// </summary>
        Top,

        /// <summary>
        /// Represents a bottom-relative coordinate.
        /// </summary>
        Bottom
    }
}